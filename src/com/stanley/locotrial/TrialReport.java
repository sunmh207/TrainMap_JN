package com.stanley.locotrial;

public class TrialReport {
	private String id;
	private String trialDate;
	private String ch;
	private String person;
	private String trialName;
	private String trialContent;
	private String requirement;
	private String locomodel;
	private String pass;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTrialName() {
		return trialName;
	}
	public void setTrialName(String trialName) {
		this.trialName = trialName;
	}
	public String getTrialContent() {
		return trialContent;
	}
	public void setTrialContent(String trialContent) {
		this.trialContent = trialContent;
	}
	public String getRequirement() {
		return requirement;
	}
	public void setRequirement(String requirement) {
		this.requirement = requirement;
	}
	public String getLocomodel() {
		return locomodel;
	}
	public void setLocomodel(String locomodel) {
		this.locomodel = locomodel;
	}
	public String getTrialDate() {
		return trialDate;
	}
	public void setTrialDate(String trialDate) {
		this.trialDate = trialDate;
	}
	public String getCh() {
		return ch;
	}
	public void setCh(String ch) {
		this.ch = ch;
	}
	public String getPerson() {
		return person;
	}
	public void setPerson(String person) {
		this.person = person;
	}
	public String getPass() {
		return pass;
	}
	public void setPass(String pass) {
		this.pass = pass;
	}

}
